package oops;

public class Greetings
{
	//1) No param  no return value

	void m1()
	{
		System.out.println("Welcome");
	}

	//2) No param  return value

	String m2()
	{
		return("How are you ??");
	}


	//3) Takes paras  No return value

	void m3(String name)
	{
		System.out.println("Hello "+name);
	}


	//4) takes paras  return value
	String m4(String sname)
	{
		return ("Hello "+sname);
	}





}
