package control_statements;

public class positiveOrNegativeNumber
{
	public static void main (String[] args)
	{
		int num=0;
		if (num>0)
		{
			System.out.println("Positive Number");
		}
		else if (num<0)
		{
			System.out.println("Negative Number");
		}
		else
		{
			System.out.println("Number is Zero");
		}
	}


}
